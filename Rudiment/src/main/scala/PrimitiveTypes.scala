object PrimitiveTypes extends App {

	val a: Byte = 1 // 1 Bytes

	val b: Short = 2 // 2 Bytes
	val c: Char = 'c' // 2 Bytes

	val d: Int = 4 // 4 Bytes
	val e: Long = 8 // 8 Bytes

	val f: Float = 0.4f // 4 Bytes
	val g: Double = 0.8 // 8 Bytes

	val h: Boolean = true // false

	val i: Unit = {} // {}

	println(a)
	println(b)
	println(c)
	println(d)
	println(e)
	println(f)
	println(g)
	println(h)
	println(i)

}